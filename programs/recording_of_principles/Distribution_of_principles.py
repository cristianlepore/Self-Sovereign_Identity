# Calcola le occorrenze totali per ogni principio
occorrenze = np.sum(dati, axis=0)

# Calcola mediana e media
mediana = np.median(occorrenze)
media = np.mean(occorrenze)

# Ordina i principi in base alla frequenza (decrescente)
indici_ordinati = np.argsort(occorrenze)[::-1]
principi_ordinati = [principi[i] for i in indici_ordinati]
occorrenze_ordinate = occorrenze[indici_ordinati]

# Colori con colormap Viridis (gradazioni di grigio)
